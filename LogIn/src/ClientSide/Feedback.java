    private void jSlider3StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider3StateChanged
        // TODO add your handling code here:
        int curr = jSlider3.getValue();
        if(curr <= 4){
            jLabel10.setForeground(Color.RED);
            jLabel10.setText("Worse");
        }
        else if(curr == 5){
            jLabel10.setForeground(Color.WHITE);
            jLabel10.setText("Average");
        }
        else if(curr <= 7){
            jLabel10.setForeground(Color.YELLOW);
            jLabel10.setText("Good");
        }
        else if(curr == 8){
            jLabel10.setForeground(Color.ORANGE);
            jLabel10.setText("Very Good");
        }
        else{
            jLabel10.setForeground(Color.GREEN);
            jLabel10.setText("Excellent");
        }
    }//GEN-LAST:event_jSlider3StateChanged

