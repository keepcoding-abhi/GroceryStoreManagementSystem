    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        try{
            FeedbackInfo fill = new FeedbackInfo();
            String comments = jTextArea1.getText();
            if(comments.length() > 255){
                JOptionPane.showMessageDialog(this,"Comments should be less than 255 characters","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            fill.setComments(jTextArea1.getText());
            fill.set(jSlider1.getValue(),jSlider2.getValue(),jSlider3.getValue(),jSlider4.getValue());
            fill.setUserName(username);
            Object obj = Contact.transmit(fill);
            if(obj instanceof Success){
                JOptionPane.showMessageDialog(this,"Thank you ! Your feedback has been recorded");
                this.setVisible(false);
                prev.setVisible(true);
                this.dispose();
            }
            else if(obj instanceof ErrorMessage){
                JOptionPane.showMessageDialog(this,"Sorry. Due to technical glitch your feedback could not be recorded.","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(IOException | ClassNotFoundException exc){
            JOptionPane.showMessageDialog(this,exc,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1MouseClicked

